export const getPreference = (key, fallback) => {
  return localStorage.getItem(key) || fallback;
};

export const setPreference = (key, value) => {
  localStorage.setItem(key, value);
  window.dispatchEvent(new Event("storage"));
};

export const applyTheme = (theme) => {
  document.body.classList.toggle("light-mode", theme === "light");
  document.body.classList.toggle("dark-mode", theme !== "light");
};

export const getProductCategory = (product) => {
  return (
    localStorage.getItem(`productCategory:id:${product.id}`) ||
    localStorage.getItem(`productCategory:name:${product.name}`) ||
    product.category ||
    "Electronics"
  );
};

export const saveProductCategory = (product, category) => {
  if (product.id) {
    localStorage.setItem(`productCategory:id:${product.id}`, category);
  }

  if (product.name) {
    localStorage.setItem(`productCategory:name:${product.name}`, category);
  }
};

export const parseOrderDetails = (customerName) => {
  const details = {
    customer: customerName || "",
    product: customerName || "Product",
    quantity: "1",
    phone: "Not added",
    address: "Not added",
    payment: "Not added",
    paymentStatus: "Not added"
  };

  if (!customerName) {
    return details;
  }

  if (customerName.includes(" | ")) {
    customerName.split(" | ").forEach((part, index) => {
      const [key, ...rest] = part.split(":");
      const value = rest.join(":").trim();

      if (index === 0) {
        details.customer = part;
      }

      if (key === "product") {
        details.product = value;
      }

      if (key === "qty") {
        details.quantity = value;
      }

      if (key === "phone") {
        details.phone = value;
      }

      if (key === "address") {
        details.address = value;
      }

      if (key === "payment") {
        details.payment = value;
      }

      if (key === "paymentStatus") {
        details.paymentStatus = value;
      }
    });

    return details;
  }

  if (customerName.includes(" - ")) {
    details.product =
      customerName.split(" - ").slice(1).join(" - ");
  }

  return details;
};
